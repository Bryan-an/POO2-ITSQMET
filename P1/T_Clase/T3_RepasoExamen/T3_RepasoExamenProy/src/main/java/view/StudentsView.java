    public boolean validateFields(String document, String name, String lastName, String grade1, String grade2, String grade3) {
        boolean correct = false;

        if (document.length() == 0
                || name.length() == 0
                || lastName.length() == 0
                || grade1.length() == 0
                || grade2.length() == 0
                || grade3.length() == 0) {

            JOptionPane.showMessageDialog(null, "Formulario vacío", "Error", JOptionPane.ERROR_MESSAGE);
            return correct;
        }

        if (document.length() != 10 || !document.matches("[0-9]{10}")) {
            JOptionPane.showMessageDialog(null, "Número de cédula inválido", "Error", JOptionPane.ERROR_MESSAGE);
            return correct;
        }

        if (!grade1.matches("[0-9]{1,2}(\\.[0-9]{1,2})?")
                || Double.parseDouble(grade1) < 0
                || Double.parseDouble(grade1) > 10
                || !grade2.matches("[0-9]{1,2}(\\.[0-9]{1,2})?")
                || Double.parseDouble(grade2) < 0
                || Double.parseDouble(grade2) > 10
                || !grade3.matches("[0-9]{1,2}(\\.[0-9]{1,2})?")
                || Double.parseDouble(grade3) < 0
                || Double.parseDouble(grade3) > 10) {

            JOptionPane.showMessageDialog(null, "Calicación inválida, las calificaciones deben tener el formato: 00.00; y deben estar en el rango [0-10]", "Error", JOptionPane.ERROR_MESSAGE);
            return correct;
        }

        correct = true;
        return correct;
    }

