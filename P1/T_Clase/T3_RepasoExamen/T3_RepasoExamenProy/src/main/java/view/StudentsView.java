    private void insertjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertjButtonActionPerformed
        String id = idjTextField.getText().trim();
        String document = documentjTextField.getText().trim();
        String name = namejTextField.getText().trim();
        String lastName = lastNamejTextField.getText().trim();
        String grade1 = grade1jTextField.getText().trim();
        String grade2 = grade2jTextField.getText().trim();
        String grade3 = grade3jTextField.getText().trim();

        if (id.length() != 0) {

            JOptionPane.showMessageDialog(null, "No puede insertar", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!validateFields(document, name, lastName, grade1, grade2, grade3)) {
            return;
        }

        controller.setDocument(document);
        controller.setName(name);
        controller.setLastName(lastName);
        controller.setGrade1(Double.parseDouble(grade1));
        controller.setGrade2(Double.parseDouble(grade2));
        controller.setGrade3(Double.parseDouble(grade3));

        boolean result = model.insertStudents(controller);

        if (result) {
            JOptionPane.showMessageDialog(null, "Estudiante insertado", "Información", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Estudiante no insertado", "Error", JOptionPane.ERROR_MESSAGE);
        }

        loadStudents("");
        cleanForm();

    }//GEN-LAST:event_insertjButtonActionPerformed

